"""Fast Analysis — artifact persistence (save/load workspace) + decoupled render.

This module is the **Capa B/C** persistence layer for the experimental
*Fast Analysis* workflow mode. It is intentionally self-contained:

* No Tkinter, no py4DSTEM imports — only ``numpy`` + ``matplotlib`` (Agg ``Figure``).
* It never touches ``single`` / ``batch`` code paths. ``pipeline.py`` is untouched.

Source of truth = numeric arrays (``strain_raw``) + ``strain_manifest.json`` +
``analysis_session.json``. PNGs are *export*, never the source of truth.

Layout written per scan (xarray/napari-ready for Phase 2)::

    gui_results_fast/<scan_stem>/
        data/
            strain_raw_without_roi.npz     # (H,W,3) fraction, channels [eyy,exx,exy]
            strain_raw_with_roi.npz        # same format (only if a W/ ROI pass exists)
            adf_realspace.npz              # (H,W) ADF for overlay / post-analysis
            strain_manifest.json           # calibration + provenance (Capa A snapshot)
            analysis_session.json          # view/analysis state (Capa C)
            INVALIDATION.md                # human + AI readable invalidation map
            stress_tensors_without_roi_Pa.npz   # compat with current export
            stress_tensors_with_roi_Pa.npz      # compat
            stress_meta_*.json                  # compat
            params.json                         # compat marker
        figures/                            # regenerable PNGs

The reload path restores ``state.strain_raw[label]`` verbatim, so
``pipeline.strain_tensor_components_from_state`` (and therefore stress, line
profiles and rendering) works again **without ever calling get_strain**.
"""
from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Callable

import numpy as np

# --------------------------------------------------------------------------- #
# Constants
# --------------------------------------------------------------------------- #

FAST_GUI_RESULTS_DIR = "gui_results_fast"

#: Channel order of the persisted ``(H,W,3)`` strain stack (py4DSTEM / notebook).
CHANNEL_ORDER = ("eyy", "exx", "exy")

#: Map-array labels we persist as full ``(H,W,3)`` tensors.
STRAIN_MAP_LABELS = ("without_roi", "with_roi")

_MANIFEST_NAME = "strain_manifest.json"
_SESSION_NAME = "analysis_session.json"
_INVALIDATION_NAME = "INVALIDATION.md"
_ADF_NAME = "adf_realspace.npz"
_ARTIFACT_VERSION = 1


INVALIDATION_MD = """\
# INVALIDATION MAP — Fast Analysis artifacts

Generated by `fast_artifacts.py`. Tells a human (and a future AI session) which
change invalidates which layer, so we only recompute what is genuinely stale.

| Change | Layer | Recompute? |
|---|---|---|
| Re-detect braggpeaks / detect params | A (pipeline) | **YES — heavy** (`find_Bragg_disks`) |
| Origin / Q pixel / basis | A | **YES — heavy** |
| `get_strain` parameters | A | **YES — heavy** |
| ROI Selector (unified) changed | A | **YES — heavy** (W/ ROI is a separate pass) |
| Strain W/O ROI vs W/ ROI | A | **YES — two distinct passes**, not a mask |
| Stress C11/C12/C44, plane stress/strain | B→C | Cheap — Hooke over saved ε |
| New / moved line, profile width | C | **NO** — resample saved array |
| vrange / vrange_theta / colormap / titles | C | **NO** — re-render |
| Refresh figures / export PNG/CSV/Summary | C | **NO** — re-export |
| Drift in line coordinates only | C | **NO** — shift endpoints + resample |
| Stats on a sub-region of an existing map | C | **NO** |

**Golden rule:** the `.npz` arrays + manifest are the source of truth. PNGs are export.

Reload is safe (no `get_strain`) when `strain_manifest.json.braggpeaks_mtime`
still matches the on-disk `braggpeaks.h5` mtime.
"""


