def save_fast_artifacts(
    state: Any,
    session: dict[str, Any] | None = None,
    *,
    output_root: str | Path | None = None,
    save_figures: bool = True,
    log: Callable[[str], None] | None = None,
) -> dict[str, Any]:
    """Persist the Fast Analysis workspace for the current scan.

    Returns a dict with ``scan_dir`` / ``data_dir`` / ``figures_dir`` and a
    ``written`` list of every file path created.
    """
    scan_dir = fast_scan_dir(state, output_root)
    data_dir = scan_dir / "data"
    fig_dir = scan_dir / "figures"
    data_dir.mkdir(parents=True, exist_ok=True)
    fig_dir.mkdir(parents=True, exist_ok=True)

    written: list[Path] = []
    strain_raw = getattr(state, "strain_raw", {}) or {}

    # --- (H,W,3) strain tensors -------------------------------------------- #
    n_tensors = 0
    for label in STRAIN_MAP_LABELS:
        S = _as_hw3(strain_raw.get(label))
        if S is None:
            continue
        path = data_dir / f"strain_raw_{label}.npz"
        np.savez_compressed(path, strain=S, channel_order=np.asarray(CHANNEL_ORDER))
        written.append(path)
        n_tensors += 1
        _log(log, f"  saved {path.name}  shape={S.shape} (channels {CHANNEL_ORDER})")
    if n_tensors == 0:
        _log(log, "  WARNING: no strain_raw tensors to save — compute strain first.")

    # --- ADF real-space ---------------------------------------------------- #
    adf = _adf_from_state(state)
    if adf is not None:
        path = data_dir / _ADF_NAME
        np.savez_compressed(path, adf=adf)
        written.append(path)
        _log(log, f"  saved {path.name}  shape={adf.shape}")

    # --- stress tensors (compat with current export) ----------------------- #
    stress = getattr(state, "stress_tensors_pa", {}) or {}
    stress_meta = getattr(state, "stress_meta", {}) or {}
    for label, sd in stress.items():
        if not isinstance(sd, dict):
            continue
        try:
            path = data_dir / f"stress_tensors_{label}_Pa.npz"
            np.savez_compressed(
                path,
                sigma_xx=np.asarray(sd["sigma_xx"]),
                sigma_yy=np.asarray(sd["sigma_yy"]),
                sigma_xy=np.asarray(sd["sigma_xy"]),
            )
            written.append(path)
        except Exception as exc:
            _log(log, f"  note: could not save stress {label}: {exc}")
            continue
        meta = stress_meta.get(label)
        if meta:
            mpath = data_dir / f"stress_meta_{label}.json"
            mpath.write_text(json.dumps(_jsonable(meta), indent=2), encoding="utf-8")
            written.append(mpath)

    # --- manifest ---------------------------------------------------------- #
    manifest = build_manifest(state)
    manifest["timestamps"]["export"] = _now_iso()
    manifest["written_files"] = [p.name for p in written]
    mpath = data_dir / _MANIFEST_NAME
    mpath.write_text(json.dumps(_jsonable(manifest), indent=2), encoding="utf-8")
    written.append(mpath)
    _log(log, f"  saved {mpath.name}")

    # --- analysis session (Capa C) ----------------------------------------- #
    session_out = _jsonable(session or {})
    if not isinstance(session_out, dict):
        session_out = {}
    session_out.setdefault("saved_at", _now_iso())
    spath = data_dir / _SESSION_NAME
    spath.write_text(json.dumps(session_out, indent=2), encoding="utf-8")
    written.append(spath)
    _log(log, f"  saved {spath.name}")

    # --- INVALIDATION.md --------------------------------------------------- #
    ipath = data_dir / _INVALIDATION_NAME
    ipath.write_text(INVALIDATION_MD, encoding="utf-8")
    written.append(ipath)

    # --- params.json marker (compat) --------------------------------------- #
    ppath = data_dir / "params.json"
    ppath.write_text(
        json.dumps(
            {
                "source": "fast_analysis",
                "stem": manifest["stem"],
                "strain_params": manifest["strain_params"],
                "roi_bounds": manifest["roi_bounds"],
                "q_pixel_size": manifest["q_pixel_size"],
                "r_pixel_nm": manifest["r_pixel_nm"],
            },
            indent=2,
        ),
        encoding="utf-8",
    )
    written.append(ppath)

    # --- figures (regenerable PNGs) ---------------------------------------- #
    if save_figures:
        for attr, prefix in (
            ("calibration_figures", "calibration"),
            ("strain_figures",      "strain"),
            ("stress_figures",      "stress"),
        ):
            figs = getattr(state, attr, {}) or {}
            for key, fig in figs.items():
                if fig is None or not hasattr(fig, "savefig"):
                    continue
                safe = str(key).replace("/", "_").replace(" ", "_")
                path = fig_dir / f"{prefix}_{safe}.png"
                try:
                    fig.savefig(path, dpi=200, bbox_inches="tight")
                    written.append(path)
                except Exception as exc:
                    _log(log, f"  note: could not save figure {key}: {exc}")

    _log(log, f"Saved Fast Analysis workspace → {scan_dir}  ({len(written)} files)")
    return {
        "scan_dir": scan_dir,
        "data_dir": data_dir,
        "figures_dir": fig_dir,
        "manifest": manifest,
        "written": written,
    }


