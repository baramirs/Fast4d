def _channel(raw_hw3: np.ndarray, name: str) -> np.ndarray:
    idx = {"eyy": 0, "exx": 1, "exy": 2}[name]
    return np.asarray(raw_hw3[..., idx], dtype=float)


def render_strain_figure(
    raw: Any,
    *,
    vrange: tuple[float, float] = (-2.0, 2.0),
    cmap: str | dict[str, str] = "RdBu_r",
    adf_overlay: Any = None,
    roi_bounds: Any = None,
    as_percent: bool = True,
    title: str | None = None,
):
    """Render εxx / εyy / εxy (+ optional ADF) from a ``(H,W,3)`` raw tensor.

    Pure render — no recompute. ``vrange`` is in percent when ``as_percent`` is
    True (matches the GUI's ``get_strain.vrange`` convention). ``cmap`` may be a
    single name or a ``{"exx":..,"eyy":..,"exy":..}`` dict. Returns a
    ``matplotlib.figure.Figure`` (Agg) ready for ``FigureCanvasTkAgg``.
    """
    from matplotlib.figure import Figure

    S = _as_hw3(raw)
    if S is None:
        raise ValueError("render_strain_figure needs a (H,W,3) strain tensor.")

    scale = 100.0 if as_percent else 1.0
    unit = "%" if as_percent else "frac"
    vmin, vmax = float(vrange[0]), float(vrange[1])

    panels = [("exx", "ε_xx"), ("eyy", "ε_yy"), ("exy", "ε_xy")]
    has_adf = False
    adf = None
    if adf_overlay is not None:
        try:
            adf = np.asarray(getattr(adf_overlay, "data", adf_overlay), dtype=float)
            has_adf = adf.ndim == 2
        except Exception:
            has_adf = False
    ncols = len(panels) + (1 if has_adf else 0)

    fig = Figure(figsize=(4.2 * ncols, 4.0), constrained_layout=True)

    def _cmap_for(ch: str) -> str:
        if isinstance(cmap, dict):
            return cmap.get(ch, "RdBu_r")
        return cmap

    for i, (ch, label) in enumerate(panels):
        ax = fig.add_subplot(1, ncols, i + 1)
        data = _channel(S, ch) * scale
        im = ax.imshow(data, cmap=_cmap_for(ch), vmin=vmin, vmax=vmax, aspect="equal")
        ax.set_title(f"{label} ({unit})")
        ax.set_xticks([])
        ax.set_yticks([])
        fig.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
        _draw_roi(ax, roi_bounds)

    if has_adf:
        ax = fig.add_subplot(1, ncols, ncols)
        ax.imshow(adf, cmap="gray", aspect="equal")
        ax.set_title("ADF")
        ax.set_xticks([])
        ax.set_yticks([])
        _draw_roi(ax, roi_bounds)

    if title:
        fig.suptitle(title, fontsize=11)
    return fig


def _draw_roi(ax, roi_bounds: Any) -> None:
    if not (isinstance(roi_bounds, (list, tuple)) and len(roi_bounds) == 4):
        return
    try:
        from matplotlib.patches import Rectangle

        x0, x1, y0, y1 = (float(v) for v in roi_bounds)
        ax.add_patch(
            Rectangle(
                (x0, y0), x1 - x0, y1 - y0,
                fill=False, edgecolor="lime", linewidth=1.4,
            )
        )
    except Exception:
        pass
