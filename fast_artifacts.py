def build_manifest(state: Any) -> dict[str, Any]:
    """Provenance + calibration snapshot. Best-effort; missing fields → null."""
    raw = getattr(state, "raw_mib_path", None)
    bragg = getattr(state, "braggpeaks_path", None)

    without = _as_hw3(getattr(state, "strain_raw", {}).get("without_roi"))
    image_shape = list(without.shape[:2]) if without is not None else None

    origin_xy = None
    cg = getattr(state, "center_guess", None)
    if isinstance(cg, (list, tuple)) and len(cg) == 2:
        # center_guess is (y, x) in this codebase's convention.
        origin_xy = [int(cg[0]), int(cg[1])]

    calstate = {
        "origin_done": bool(getattr(state, "origin_fit", None) is not None
                            or getattr(state, "origin_measurement", None) is not None),
        "ellipse_done": bool(getattr(state, "p_ellipse", None) is not None),
        "q_pixel_done": getattr(state, "q_pixel_size", None) is not None,
        "basis_done": bool(getattr(state, "strainmap_full", None) is not None
                           or getattr(state, "strainmap_roi", None) is not None),
        "strain_without_roi": "without_roi" in (getattr(state, "strain_raw", {}) or {}),
        "strain_with_roi": "with_roi" in (getattr(state, "strain_raw", {}) or {}),
    }

    manifest: dict[str, Any] = {
        "artifact_version": _ARTIFACT_VERSION,
        "stem": scan_stem(state),
        "raw_path": str(raw) if raw is not None else None,
        "image_shape": image_shape,
        "r_pixel_nm": getattr(state, "image_pixel_size", None),
        "r_pixel_units": getattr(state, "image_pixel_units", "nm"),
        "q_pixel_size": getattr(state, "q_pixel_size", None),
        "q_pixel_units": getattr(state, "q_pixel_units", "A^-1"),
        "origin_xy": origin_xy,
        # g1/g2 fitted reciprocal vectors live inside the StrainMap object and are
        # not reliably recoverable post-hoc; recorded as null when unknown.
        "g1_qxy": None,
        "g2_qxy": None,
        "calstate": calstate,
        "strain_params": _jsonable(getattr(state, "strain_params", {})),
        "strain_basis_params": _jsonable(getattr(state, "strain_basis_params", {})),
        "roi_bounds": _jsonable(getattr(state, "roi_bounds", None)),
        "strain_scan_roi_bounds": _jsonable(getattr(state, "strain_scan_roi_bounds", None)),
        "braggpeaks_path": str(bragg) if bragg is not None else None,
        "braggpeaks_mtime": _file_mtime(bragg),
        "channel_order": list(CHANNEL_ORDER),
        "units": {"strain": "fraction", "stress": "Pa"},
        "timestamps": {"compute": _now_iso()},
        # xarray-ready schema hint for Phase 2 (napari / xarray.Dataset(dim=scan)).
        "layout": {
            "dims": ["y", "x"],
            "strain_vars": {"eyy": 0, "exx": 1, "exy": 2},
            "stress_vars": ["sigma_xx", "sigma_yy", "sigma_xy"],
            "extra_vars": ["adf"],
        },
    }
    return manifest


