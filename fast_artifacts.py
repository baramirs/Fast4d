def _resolve_data_dir(path: str | Path) -> Path:
    """Accept a scan dir, its ``data`` subdir, or the manifest file itself."""
    p = Path(path).expanduser()
    if p.is_file() and p.name == _MANIFEST_NAME:
        return p.parent
    if (p / "data" / _MANIFEST_NAME).is_file():
        return p / "data"
    if (p / _MANIFEST_NAME).is_file():
        return p
    # Default: treat as data dir.
    return p


def manifest_is_fresh(manifest: dict[str, Any]) -> tuple[bool, str]:
    """True when braggpeaks.h5 on disk still matches the recorded mtime."""
    bp = manifest.get("braggpeaks_path")
    rec = manifest.get("braggpeaks_mtime")
    if not bp:
        return True, "no braggpeaks recorded — reload arrays as-is"
    cur = _file_mtime(bp)
    if cur is None:
        return True, f"braggpeaks file not found ({bp}) — reload arrays as-is"
    if rec is None:
        return True, "no recorded mtime — reload arrays as-is"
    if abs(float(cur) - float(rec)) < 1e-6:
        return True, "braggpeaks mtime matches — reload is safe (no get_strain)"
    return False, "braggpeaks.h5 changed since save — Capa A may be stale; recompute recommended"


def load_fast_artifacts(
    state: Any,
    path: str | Path,
    *,
    log: Callable[[str], None] | None = None,
) -> dict[str, Any]:
    """Hydrate ``state`` from a saved Fast Analysis workspace — **no get_strain**.

    Restores ``strain_raw`` tensors, ADF, calibration scalars, strain params,
    ROI bounds and braggpeaks path. Returns the manifest plus a freshness flag
    and the parsed session under keys ``manifest`` / ``session`` / ``fresh`` /
    ``freshness_reason`` / ``data_dir``.
    """
    data_dir = _resolve_data_dir(path)
    mpath = data_dir / _MANIFEST_NAME
    if not mpath.is_file():
        raise FileNotFoundError(f"No {_MANIFEST_NAME} in {data_dir}")

    manifest = json.loads(mpath.read_text(encoding="utf-8"))
    _log(log, f"Loading Fast Analysis workspace from {data_dir}")

    # --- strain tensors ---------------------------------------------------- #
    if not hasattr(state, "strain_raw") or state.strain_raw is None:
        state.strain_raw = {}
    n = 0
    for label in STRAIN_MAP_LABELS:
        npz = data_dir / f"strain_raw_{label}.npz"
        if not npz.is_file():
            continue
        with np.load(npz, allow_pickle=False) as d:
            arr = np.asarray(d["strain"], dtype=np.float32)
        state.strain_raw[label] = arr
        n += 1
        _log(log, f"  loaded strain_raw[{label!r}] shape={arr.shape}")
    if n == 0:
        _log(log, "  WARNING: no strain_raw_*.npz found in workspace.")

    # --- ADF --------------------------------------------------------------- #
    adf_path = data_dir / _ADF_NAME
    if adf_path.is_file():
        try:
            with np.load(adf_path, allow_pickle=False) as d:
                adf = np.asarray(d["adf"], dtype=np.float32)
            if not hasattr(state, "virtual_images") or state.virtual_images is None:
                state.virtual_images = {}
            # Stored as a bare ndarray; downstream uses getattr(obj,"data",obj).
            state.virtual_images["annular_dark_field"] = adf
            _log(log, f"  loaded adf_realspace shape={adf.shape}")
        except Exception as exc:
            _log(log, f"  note: could not load ADF: {exc}")

    # --- stress tensors (optional) ----------------------------------------- #
    if not hasattr(state, "stress_tensors_pa") or state.stress_tensors_pa is None:
        state.stress_tensors_pa = {}
    for label in STRAIN_MAP_LABELS:
        sp = data_dir / f"stress_tensors_{label}_Pa.npz"
        if not sp.is_file():
            continue
        try:
            with np.load(sp, allow_pickle=False) as d:
                state.stress_tensors_pa[label] = {
                    "sigma_xx": np.asarray(d["sigma_xx"]),
                    "sigma_yy": np.asarray(d["sigma_yy"]),
                    "sigma_xy": np.asarray(d["sigma_xy"]),
                }
        except Exception:
            continue

    # --- calibration scalars + params -------------------------------------- #
    def _set(attr: str, value: Any) -> None:
        if value is not None and hasattr(state, attr):
            try:
                setattr(state, attr, value)
            except Exception:
                pass

    raw_path = manifest.get("raw_path")
    if raw_path:
        try:
            state.raw_mib_path = Path(raw_path)
        except Exception:
            pass
    _set("image_pixel_size", manifest.get("r_pixel_nm"))
    _set("q_pixel_size", manifest.get("q_pixel_size"))
    if manifest.get("strain_params"):
        _set("strain_params", manifest["strain_params"])
    if manifest.get("strain_basis_params"):
        _set("strain_basis_params", manifest["strain_basis_params"])
    rb = manifest.get("roi_bounds")
    if isinstance(rb, (list, tuple)) and len(rb) == 4:
        _set("roi_bounds", tuple(int(v) for v in rb))
    srb = manifest.get("strain_scan_roi_bounds")
    if isinstance(srb, (list, tuple)) and len(srb) == 4:
        _set("strain_scan_roi_bounds", tuple(int(v) for v in srb))
    bp = manifest.get("braggpeaks_path")
    if bp:
        try:
            state.braggpeaks_path = Path(bp)
        except Exception:
            pass

    # --- analysis session -------------------------------------------------- #
    session: dict[str, Any] = {}
    spath = data_dir / _SESSION_NAME
    if spath.is_file():
        try:
            session = json.loads(spath.read_text(encoding="utf-8"))
        except Exception as exc:
            _log(log, f"  note: could not parse session: {exc}")

    fresh, reason = manifest_is_fresh(manifest)
    _log(log, f"  freshness: {'FRESH' if fresh else 'STALE'} — {reason}")

    return {
        "manifest": manifest,
        "session": session,
        "fresh": fresh,
        "freshness_reason": reason,
        "data_dir": data_dir,
    }


