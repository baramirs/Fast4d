def _log(log: Callable[[str], None] | None, msg: str) -> None:
    if log is not None:
        try:
            log(msg)
        except Exception:
            pass


def _jsonable(obj: Any) -> Any:
    """Best-effort conversion of numpy / Path / tuple structures to JSON types."""
    if obj is None or isinstance(obj, (bool, int, float, str)):
        return obj
    if isinstance(obj, Path):
        return str(obj)
    if isinstance(obj, dict):
        return {str(k): _jsonable(v) for k, v in obj.items()}
    if isinstance(obj, (list, tuple)):
        return [_jsonable(v) for v in obj]
    if isinstance(obj, (np.integer,)):
        return int(obj)
    if isinstance(obj, (np.floating,)):
        return float(obj)
    if isinstance(obj, (np.bool_,)):
        return bool(obj)
    if isinstance(obj, np.ndarray):
        return obj.tolist()
    # Fall back to string so json.dump never crashes the save.
    try:
        return str(obj)
    except Exception:
        return None


def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat(timespec="seconds")


def scan_stem(state: Any) -> str:
    raw = getattr(state, "raw_mib_path", None)
    if raw is not None:
        try:
            return Path(raw).stem
        except Exception:
            pass
    return "results"


def fast_results_root(state: Any, output_root: str | Path | None = None) -> Path:
    """``<raw parent>/gui_results_fast`` by default; never ``gui_results/`` (single)."""
    if output_root is not None:
        return Path(output_root).expanduser()
    raw = getattr(state, "raw_mib_path", None)
    base = Path(raw).parent if raw is not None else Path.cwd()
    return base / FAST_GUI_RESULTS_DIR


def fast_scan_dir(state: Any, output_root: str | Path | None = None) -> Path:
    return fast_results_root(state, output_root) / scan_stem(state)


def _structured_to_hw3(S: np.ndarray) -> np.ndarray | None:
    """Structured strain array (named fields) → ``(H,W,3)`` stack [eyy, exx, exy]."""
    names = {str(n).lower(): n for n in (S.dtype.names or ())}

    def pick(*cands: str) -> Any:
        for c in cands:
            if c in names:
                return names[c]
        return None

    n_eyy = pick("e_yy", "eyy", "eps_yy")
    n_exx = pick("e_xx", "exx", "eps_xx")
    n_exy = pick("e_xy", "exy", "eps_xy")
    if not (n_eyy and n_exx and n_exy):
        return None
    try:
        return np.stack(
            [
                np.asarray(S[n_eyy], dtype=float),
                np.asarray(S[n_exx], dtype=float),
                np.asarray(S[n_exy], dtype=float),
            ],
            axis=-1,
        )
    except Exception:
        return None


def _as_hw3(arr: Any) -> np.ndarray | None:
    """Coerce a raw strain entry to ``(H,W,3)`` float32 [eyy, exx, exy], or ``None``.

    py4DSTEM ``strainmap_g1g2.data`` often carries extra channels (theta, mask) and
    may put the channel axis first, e.g. ``(5,H,W)`` or ``(H,W,5)``. We mirror
    ``pipeline._strain_channel``: the channel axis is the last (or first) axis with
    size 3–8, and we keep the first three channels (eyy, exx, exy) by index.
    """
    if arr is None:
        return None
    try:
        S = np.asarray(arr)
    except Exception:
        return None
    if S.dtype.names:  # structured array → stack named fields
        st = _structured_to_hw3(S)
        if st is None:
            return None
        S = st
    S = np.squeeze(S)
    if S.ndim != 3:
        return None
    if 3 <= S.shape[-1] <= 8:
        chan_last = S
    elif 3 <= S.shape[0] <= 8:
        chan_last = np.moveaxis(S, 0, -1)
    else:
        return None
    # Keep eyy/exx/exy (channels 0,1,2); drop theta/mask if present.
    return np.ascontiguousarray(chan_last[..., :3], dtype=np.float32)


def _adf_from_state(state: Any) -> np.ndarray | None:
    vis = getattr(state, "virtual_images", None) or {}
    for key in ("annular_dark_field", "ADF", "adf"):
        obj = vis.get(key)
        if obj is None:
            continue
        try:
            arr = np.asarray(getattr(obj, "data", obj), dtype=np.float32)
        except Exception:
            continue
        if arr.ndim == 2:
            return np.ascontiguousarray(arr)
    return None


def _file_mtime(path: Any) -> float | None:
    try:
        p = Path(path)
        if p.is_file():
            return float(p.stat().st_mtime)
    except Exception:
        pass
    return None


